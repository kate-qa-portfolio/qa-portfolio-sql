/* =========================================================
1. Таблиця Employees.
Напишіть запит для вибору імен, прізвищ, окладів усіх 
працівників та їх id департаменту, оклад яких не
перевищує 10000.
========================================================= */

SELECT first_name,
last_name,
salary,
department_id
FROM HR.employees
WHERE salary <= 10000;

/* =========================================================
2. Таблиця Employees.
Напишіть запит для відображення імен, прізвищ, ідентифікаторів 
посад та комісійних винагород всіх працівників, які не мають права
на отримання комісійної винагороди.
========================================================= */

SELECT first_name,
last_name,
job_id,
commission_pct
FROM HR.employees
WHERE commission_pct IS NULL;

/* =========================================================
3. Таблиця Employees.
Напишіть 2 варіанти запиту для відображення списку всіх імен,
 прізвищ та їх id департаменту, де імʼя співробітника John.
========================================================= */

